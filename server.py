def add(x: int,y: int)->int:
    """Add 2 numbers and return its sum"""
    return x+y

@mcp.tool
def add_expense(expense: Expense):

    cursor.execute("""
        INSERT INTO expenses (
            expense_detail,
            expense_amount,
            expense_category
        )
        VALUES (?, ?, ?)
    """, (
        expense.expense_detail,
        expense.expense_amount,
        expense.expense_category
    ))

    conn.commit()

    return "Expense added successfully."


@mcp.tool
def get_all_expenses():

    cursor.execute("""
        SELECT
            id,
            expense_detail,
            expense_amount,
            expense_category,
            expense_date
        FROM expenses
        ORDER BY expense_date DESC
    """)

    rows = cursor.fetchall()

    expenses = []

    for row in rows:
        expenses.append({
            "id": row[0],
            "expense_detail": row[1],
            "expense_amount": row[2],
            "expense_category": row[3],
            "expense_date": row[4]
        })

    return expenses




