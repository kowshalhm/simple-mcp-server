    expense_amount : float=Field(gt=0,description="Expense amount.")
    expense_category: Literal["Education","Transport","Health","Entertainment","Leisure","Dining","Groceries","Rent","Utilities","Miscellaneous"]=Field(description="Category of expense ; if category cannot be assessed then classify as 'Miscellaneous'")


